    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (this.checkEmptyFields()) {
            if(Helper.isRadioButtonSelected(ansGroup)) {
                Question questionBean = new Question();
                questionBean.setQuestion(txtAreaQuestion.getText());
                questionBean.setAnswer1(txtAns1.getText());
                questionBean.setAnswer2(txtAns2.getText());
                questionBean.setAnswer3(txtAns3.getText());
                questionBean.setAnswer4(txtAns4.getText());
                int correctAns = Integer.parseInt(Helper.getSelectedButtonText(ansGroup));
                questionBean.setCorrectAns(correctAns);
                
                QuestionManager.insertQuestion(questionBean);
                JOptionPane.showMessageDialog(null, "New question added succesfully.", "Add Question", JOptionPane.INFORMATION_MESSAGE);                            
                this.clearFields();
            } else {
                JOptionPane.showMessageDialog(null, "Please select the answer for the question before you add.", "Add Question", JOptionPane.WARNING_MESSAGE);                            
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please fill out all the textfields with content", "Add Question", JOptionPane.WARNING_MESSAGE);            
        }
    }//GEN-LAST:event_btnAddActionPerformed

