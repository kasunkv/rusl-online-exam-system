    private boolean checkEmptyFields() {
        boolean question = (txtAreaQuestion.getText().equals("")) ? false : true;
        boolean answer1 = (txtAns1.getText().equals("")) ? false : true;
        boolean answer2 = (txtAns2.getText().equals("")) ? false : true;
        boolean answer3 = (txtAns3.getText().equals("")) ? false : true;
        boolean answer4 = (txtAns4.getText().equals("")) ? false : true;
        
        if (question && answer1 && answer2 && answer3 && answer4) {
            return true;
        } else {
            return false;
        }
    }

